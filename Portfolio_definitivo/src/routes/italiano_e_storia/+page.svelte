<div class="paragrafo2">
    <div class="title2">Le gerarchie mafiose</div>
    <div class="altro">
        <img src="./gerarchia_mafiosa.jpeg" alt="Gerarchia Mafiosa">
    </div>
    <div class="testo"> 
        <p>
            Le famiglie mafiose non sono tutte uguali, ogni famiglia mafiosa ha le sue regole e le sue tradizioni.
            In generale l'ingresso in una famiglia mafiosa comporta sempre un piccolo rito di iniziazione in cui si giura fedeltà alla famiglia e successivamente si compie un gesto simbolico come ad esempio bruciare un santino o un patto di sangue.
            Una volta all'interno della famiglia è presente una fortissima gerarchia.
            Il capo supremo prende il nome di "boss" e prende tutte le decisioni aiutato da un consigliere, sotto di lui c'è il vice-boss e sotto ancora dei capi famiglia che gestiscono gruppi più piccoli di quelli che vengono chiamati "soldati".
            Gli associati non sono membri effettivi della famiglia, ma sono coloro che aiutano i soldati nelle attività criminali
        </p>
    </div>
</div> <br><hr style="width: 60%;"><br>

<div class="paragrafo1">
    <div class="title">Il pool antimafia</div>
    <div class="altro">
            <img src="./pool_antimafia.jpeg" alt="Pool antimafia">
    </div>
    <div class="testo"> 
        <p>
            Il pool antimafia era un gruppo di magistrati, fondato da Rocco Chinnici, con l'obbiettivo di combattere la mafia.
            L'idea del pool nasce in seguito a diversi assassinii di magistrati che indagavano sulla mafia o sul terrorismo.
            Quando un magistrato lavora da solo è esposto a un rischio molto più elevato, ma se facente parte di un pool e condividendo informazioni con gli altri membri, i magistrati e le informazioni erano più protetti.
            Alcuni dei membri più conosciuti furono Paolo Borsellino, Giovanni Falcone e Antonio Caponetto.
            Grazie alle informazioni ottenute delle loro indagini è stato possibile avviare il famosissimo maxiprocesso . 
        </p>
    </div>
</div><br><hr style="width: 60%;"><br>


<div class="paragrafo2">
    <div class="title2">Il maxiprocesso</div>
    <div class="altro">
        <img src="./aula_bunker.jpeg" alt="Aula bunker">
    </div>
    <div class="testo"> 
        <p>
            Maxiprocesso di Palermo è la denominazione che fu data, a livello giornalistico, a un processo penale svoltosi a Palermo per crimini di mafia, tra cui omicidio, traffico di stupefacenti, estorsione, associazione mafiosa e altri.
            Il maxiprocesso deve il proprio soprannome alle sue enormi proporzioni: in primo grado gli imputati erano 475, con circa 200 avvocati difensori. Il processo di primo grado si concluse con 19 ergastoli e pene detentive per un totale di 2665 anni di reclusione. A quanto è dato sapere, si tratta del più grande processo penale mai celebrato al mondo.
            Durò dal 10 febbraio 1986 (giorno di inizio del processo di primo grado) al 30 gennaio 1992 (giorno della sentenza finale della Corte di Cassazione). 
        </p>
    </div>
</div> <br><hr style="width: 60%;"><br>



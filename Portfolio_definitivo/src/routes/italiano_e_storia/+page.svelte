    <div class="testo"> 
        <br><br>
        <div class="titoletto">STRAGE DI VIA D'AMELIO:</div>
        <p>
            In questa strage molto conosciuta, avvenuta domenica 19 luglio 1992, ha perso la vita il magistrato Paolo Borsellino.
            Era andato a prendere sua madre per portarla a una visita, ma quando suonò il campanello un'auto-bomba parcheggiata lì affianco saltò in aria, lui morì sul colpo, ma qualcuno appena scoppiata la bomba aprì la sua macchina, ormai in fiamme, e recuperò la sua agenda rossa, agenda su cui erano riportate importantissime testimonianze di pentiti che avrebbero fatto finire in carcere grandi boss mafiosi.
            Ci furono grandi dibattiti su chi fu stato a rubarla, se esistesse realmente, se fosse effettivamente in quella macchina, ma la verità non fu mai scoperta con certezza e questo evento viene ancora ricordato oggi come uno dei più grandi depistaggi della storia.
        </p>
    </div>
